package main.java.com.nadee.oop.inheritance;

/*
 * Inheritance is the process by which objects of one class acquire some properties of objects of another class. 
 * Inheritance supports the concept of hierarchical classification.
 * The principle behind this division is that each subclass shares common characteristics from the class from its parent class.
 */
public class Inheritance {

	public static void main(String[] args) {
		Dog d = new Dog();
		d.eat();
		d.nature();
		d.sound();
	}

}
